package session3.Challenges;

import java.util.Scanner;

public class LogicaComplementedNegationOperation2 {
    public static void main(String[] args) {

        Scanner in = new Scanner(System.in);
        System.out.print("First Boolean - ");
        boolean value1 = in.nextBoolean();
        System.out.println("The opposite value is - " + !value1);
        System.out.print("Second Boolean - ");
        boolean value2 = in.nextBoolean();
        System.out.println("The opposite value is - " + !value2);





    }
}
