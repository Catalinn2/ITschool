package session3.Challenges;

public class IncrementandDecrementOperators {
    public static void main(String[] args) {
        int i;
        for (i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (i = 10; i > 0; i--) {
            System.out.println(i);
        }


    }
}
